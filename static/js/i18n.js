/**
 * Get a translated string by key.
 */
export function t(key, ...args) {
    const dict = LANGUAGES[_currentLang] || LANGUAGES.en;
    const val = dict[key] ?? LANGUAGES.en[key] ?? key;
    if (typeof val === "function") return val(...args);
    return val;
}

